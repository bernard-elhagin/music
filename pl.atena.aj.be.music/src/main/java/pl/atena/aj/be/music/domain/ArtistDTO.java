package pl.atena.aj.be.music.domain;

import java.io.Serializable;

public class ArtistDTO implements Serializable {

	private static final long serialVersionUID = -8764908227192375066L;

	/* ID wykonawcy w bazie */
	private int artistId;

	/* Nazwa wykonawcy */
	private String name;

	public ArtistDTO() { }

	public int getArtistId() {
		return artistId;
	}

	public void setArtistId(int artistId) {
		this.artistId = artistId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return "ArtistDTO [artistId=" + artistId + ", name=" + name + "]";
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + artistId;
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		ArtistDTO other = (ArtistDTO) obj;
		if (artistId != other.artistId) {
			return false;
		}
		if (name == null) {
			if (other.name != null) {
				return false;
			}
		} else if (!name.equals(other.name)) {
			return false;
		}
		return true;
	}
}